def greaterThen(x, y):
    if x > y:
        return True
    else:
        return False
def lessThen(x, y):
    if x < y:
        return True
    else:
        return False
def equalTo(x, y):
    if x == y:
        return True
    else:
        return False
def greaterOrEqual(x, y):
    if x >= y:
        return True
    else:
        return False
def lessOrEqual(x, y):
    if x <= y:
        return True
    else:
        return False

